class User < ApplicationRecord
  def password=(value)
    super(BCrypt::Password.create(value))
  end

  validates :email, presence: {message: 'field cannot empty'}
  validates :firstname, presence: {message: 'field cannot empty'}
  validates :lastname, presence: {message: 'field cannot empty'}
  validates :password, confirmation: true
  validates :password_confirmation, presence: true
  validates :email, format: {with: /(?:[a-z0-9!#$%&'*+\=?^_`{|}~-]+(?:\.[a-z0-9!#$%&'*+\=?^_`{|}~-]+)*|"(?:[\x01-\x08\x0b\x0c\x0e-\x1f\x21\x23-\x5b\x5d-\x7f]|\\[\x01-\x09\x0b\x0c\x0e-\x7f])*")@(?:(?:[a-z0-9](?:[a-z0-9-]*[a-z0-9])?\.)+[a-z0-9](?:[a-z0-9-]*[a-z0-9])?|\[(?:(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\.){3}(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?|[a-z0-9-]*[a-z0-9]:(?:[\x01-\x08\x0b\x0c\x0e-\x1f\x21-\x5a\x53-\x7f]|\\[\x01-\x09\x0b\x0c\x0e-\x7f])+)\])/, message: 'format is invalid!'}

  has_and_belongs_to_many :budgets

end
